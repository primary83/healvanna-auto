// Reverse lookup: slug -> deal ID
export const SLUG_TO_ID: Record<string, string> = Object.fromEntries(
  Object.entries(SLUG_MAP).map(([id, slug]) => [slug, id])
);

export function getDealBySlug(slug: string) {
  const id = SLUG_TO_ID[slug];
  return id ? EV_DEALS.find((d) => d.id === id) : undefined;
}

export function getSlugForDeal(dealId: string): string {
  return SLUG_MAP[dealId] || dealId;
}

